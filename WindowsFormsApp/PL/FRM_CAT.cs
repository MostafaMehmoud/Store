        private void btn_delete_Click(object sender, EventArgs e)
        {
            Toast toast = new Toast();
            Diolag diolag = new Diolag();
            try
            {
                id =Convert.ToInt32(tileView1.GetFocusedRowCellValue("ID"));
           var rs= MessageBox.Show("عملية حذف", "هل انت متاكد من عملية الحذف", MessageBoxButtons.YesNo);
           if (rs == DialogResult.Yes)
            {
                    TB_CAT = db.TB_CAT.Where(x => x.ID == id).FirstOrDefault();
                db.Entry(TB_CAT).State = System.Data.Entity.EntityState.Deleted;
                db.SaveChanges();
                    toast.txt_Caption.Text = "تم حذف الصنف";
                    toast.Show();
                Update_data();
            }
            }catch 
            {
                diolag.txt_Caption.Text = "لا يوجد صنف لحذفة";
                diolag.Width=this.Width;
                diolag.Show();
            }
           
            
        }

        private void Update_data()
        {
