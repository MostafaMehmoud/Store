        }

        private void btn_edit_Click(object sender, EventArgs e)
        {
            FRM_CAT_ADD fRM_CAT_ADD = new FRM_CAT_ADD();
            id = Convert.ToInt32(tileView1.GetFocusedRowCellValue("ID"));

            TB_CAT = db.TB_CAT.Where(x => x.ID == id).FirstOrDefault();
           fRM_CAT_ADD.edt_name.Text=TB_CAT.CAT_name.ToString();
            methods.by = TB_CAT.CAT_cover;
            fRM_CAT_ADD.pic_Cover.Image = Image.FromStream(methods.Convert_image());
            fRM_CAT_ADD.id = id ;
            fRM_CAT_ADD.btn_add.Text = "تعديل";
            fRM_CAT_ADD.Show();
        }

